function initTable() {
    var group = getCookie("currentGroup");
    $.ajax({
        url: "rest/wallposts/" + group,
        type: "GET",
        dataType: "json",
        contentType: "application/json",
        success: function (data) {
            if (data.length === 0){
                $(".textPosts").append("<div class='page-header'><h1>The are no posts in the feed.</h1></div>");
            } else {
            var id = 0, postedTo = 0;
            var message = "", postedBy = "";
            var datePosted="";
            for (var i = data.length-1; i >= 0; i--) {
                id = data[i].id;
                datePosted = new Date(data[i].datePosted);
                message = data[i].message;
                postedBy = data[i].postedBy;
                postedTo = data[i].postedTo;
                $(".textPosts").append($("<div class='post'><div class='message'><p>" +
                    message + "</p></div><div class='postedBy'><p>"+ postedBy +
                    "</p></div><div class='datePosted'><p>" + datePosted.toLocaleDateString() + " at " +
                    datePosted.toLocaleTimeString() +"</p></div></div>"));
            }
            }
        },
        error: function () {
            //TODO: fix, do something else then return error.html.
            $(".textPosts").append("<div class='page-header'><h1>Something went wrong. Try again.s</h1></div>");
        }
    })
}

